public class evanodd {   
    public static void main(String[] args) {
        for (int num = 1; num <= 20; num++) {
            if (num % 2 == 0) {
                System.out.println(num + " is Even");
            } else {
                System.out.println(num + " is Odd");
            }
        }
    }
}

    

